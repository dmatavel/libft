#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (*(int *)s && i < n)
	{
		*(int *)s = c;
		i++;
		s++;
	}
	return ((char *)s);
}
