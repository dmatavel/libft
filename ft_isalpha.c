int	ft_isalpha(char *str)
{
	while (*str)
	{
		if ((*str >= 'a' && *str <= 'z')
			|| (*str >= 'A' && *str <= 'Z'))
		{
		str++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
